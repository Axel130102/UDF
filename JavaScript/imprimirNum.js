//Imprimir numero del 0 al 100 en orden ascendente y descendente 

// Imprimir números del 0 al 100 en orden ascendente
//le asignamos una varible que en este caso es i,
//donde a esa i le decimos que este ciclo se va a repetir mientras i que tiene un valor de 0 alcance el mismo valor que le dimos 
//en este caso 100 y la siguiente instruccion es que nuestra i va irse sumando
//para que se muestre mostramos en consola cada ciclo que realiza para que se forme del 1 al 100
console.log("Numeros ascendente");
for (let i = 0; i <= 100; i++) { 
    console.log(i);
  }

// Imprimir números del 100 al 0 en orden descendente
//le asignamos una varible que en este caso es i,
//donde a esa i le decimos que este ciclo se va a repetir mientras i que tiene un valor de 100 alcance el mismo valor que le dimos 
//en este caso 0 y la siguiente instruccion es que nuestra i va irse restando
//para que se muestre mostramos en consola cada ciclo que realiza para que se forme del 100 al 0
console.log("Numeros descendente");
for (let i = 100; i >= 0; i--) {
    console.log(i);
  }
  
  